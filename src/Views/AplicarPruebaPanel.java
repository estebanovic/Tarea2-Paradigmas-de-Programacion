    private void mostrarPregunta(int indice) {
        this.indiceActual = indice;

        Pregunta pregunta = prueba.getPreguntas().get(indice);
        enunciado.setText((indice + 1) + ". " + pregunta.getEnunciado());

        // Limpiar panel y grupo anterior
        panelOpciones.removeAll();
        grupoOpciones = new ButtonGroup();

        for (Opcion o : pregunta.getOpciones()) {
            JRadioButton rb = new JRadioButton(o.getTexto());

            // REGISTRA EVENTO para habilitar el bot贸n "Siguiente"
            rb.addActionListener(e -> btnSiguiente.setEnabled(true));

            grupoOpciones.add(rb);
            panelOpciones.add(rb);

            // Restaurar selecci贸n si ya respondi贸 antes
            if (respuestasUsuario.size() > indice && respuestasUsuario.get(indice).equals(o.getTexto())) {
                rb.setSelected(true);
            }
        }

        panelOpciones.revalidate();
        panelOpciones.repaint();

        btnAnterior.setEnabled(indice > 0);
        btnAnterior.setText("Anterior");
        btnSiguiente.setEnabled(false);
        btnSiguiente.setText((indice == prueba.getPreguntas().size() - 1) ? "Finalizar" : "Siguiente");
    }

    private void guardarRespuestaActual() {
        for (Component c : panelOpciones.getComponents()) {
            if (c instanceof JRadioButton rb && rb.isSelected()) {
                if (respuestasUsuario.size() > indiceActual) {
                    respuestasUsuario.set(indiceActual, rb.getText());
                } else {
                    respuestasUsuario.add(rb.getText());
                }
                return;
            }
        }
        // Si no seleccion贸 nada
        if (respuestasUsuario.size() <= indiceActual) {
            respuestasUsuario.add("");
        }
    }

    private void finalizarPrueba() {
        guardarRespuestaActual();

        int correctas = 0;
        for (int i = 0; i < prueba.getPreguntas().size(); i++) {
            Pregunta pregunta = prueba.getPreguntas().get(i);
            String resp = respuestasUsuario.get(i);

            for (Opcion o : pregunta.getOpciones()) {
                if (o.isEsCorrecta() && o.getTexto().equals(resp)) {
                    correctas++;
                }
            }
        }

        double nota = ((correctas * 6.0) / prueba.getPreguntas().size()) + 1;

        ResultadoPanel resultado = new ResultadoPanel(mainFrame, prueba, respuestasUsuario, nota);
        mainFrame.getMainPanel().add(resultado, "resultado");
        mainFrame.mostrarVista("resultado");
    }

