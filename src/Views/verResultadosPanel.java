    private void cargarResultados() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0); // limpiar

        ResultadoController controller = new ResultadoController();
        List<Resultado> resultados = controller.listarResultados();

        for (Resultado r : resultados) {
            modelo.addRow(new Object[]{
                r.getId(),
                r.getNombrePrueba(),
                r.getFecha(),
                r.getNota(),
                "Revisar"
            });
        }
    }
    
    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.rowAtPoint(evt.getPoint());
        int col = jTable1.columnAtPoint(evt.getPoint());
        
        if (col == 4) { // Columna "AcciÃ³n", ahora es la quinta columna (Ã­ndice 4)
               int resultadoId = (int) jTable1.getValueAt(row, 0);
               System.out.println("ðŸ“‹ Revisar resultado con ID: " + resultadoId);

               ResultadoController rc = new ResultadoController();
               Resultado resultado = rc.obtenerResultadoPorId(resultadoId); // este mÃ©todo lo vemos ahora
               rc.cargarRespuestasEnResultado(resultado);

               Prueba prueba = new PruebaController().cargarPruebaCompleta(resultado.getPruebaId());

               List<String> respuestasUsuario = resultado.getRespuestas().stream()
                       .map(Respuesta::getOpcionMarcada)
                       .toList();

               RevisarPruebaPanel panel = new RevisarPruebaPanel(mainFrame, prueba, respuestasUsuario);
               mainFrame.getMainPanel().add(panel, "revisarPrueba");
               mainFrame.mostrarVista("revisarPrueba");
           }
    }//GEN-LAST:event_jTable1MouseClicked

